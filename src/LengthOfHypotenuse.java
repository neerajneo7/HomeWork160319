public class LengthOfHypotenuse
{
    // This programme is to find the hypotenuse value when 2 sides of the right triangle is given
    public static void main(String[] args)
    {
        int a = 10;
        int b = 20;
        System.out.println("The length of hypotenuse is : " +Math.hypot(a, b));
        // +Mathhypot is the pre defined formulae for hypotenuse in IntelliJ
    }




}
